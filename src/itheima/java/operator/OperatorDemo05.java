package itheima.java.operator;


//关系运算符
public class OperatorDemo05 {
    public static void main(String[] args) {
        int i = 10;
        int j = 5;

        System.out.println(i == j);
        System.out.println(i > j);
        System.out.println(i < j);
        System.out.println(i <= j);
        System.out.println(i = j);
    }
}
