package itheima.java.loop;

// for语句的学习
public class ForDemo01 {
    public static void main(String[] args) {
        for (int i = 0;i < 5;i++){
            System.out.println("HelloWorld");
        }
        System.out.println("--------------");
        for (int i = 0;i <= 5;i += 2){
            System.out.println("HellWorld2");
        }
    }
}
