package itheima.java.type;

//自动类型转换
/* */
public class TypeDemo01 {
    public static void main(String[] args) {
        byte a = 20;
        int b = a;
        System.out.println(a);
        System.out.println(b);

        int age = 23;
        double db = age;
        System.out.println(db);

        char ch = 'a';
        int cha = ch;
        System.out.println(cha);

    }
}
